class Solution:
    def lengthOfLongestSubstring(self, s: str) -> int:
        d = defaultdict(int)
        cnt = 0
        ans, j = 0, 0
        for i in range(len(s)):
            c = s[i]
            d[c] += 1
            if d[c] == 2:
                cnt += 1
            while j < i and cnt > 0:
                c = s[j]
                d[c] -= 1
                if d[c] == 1:
                    cnt -= 1
                j += 1
            ans = max(ans, i - j + 1)
        return ans
